import { useReducer, useContext, ReactNode } from "react";
import CasesContext from "./casesContext";
import AppReducer from "../AppReducer";
import InViewContext from "../../context/inView/inViewContext";

import { toast } from "react-toastify";
import { get, post, put, del } from "../../utils/fetch";
import { GET_CASES } from "../types";
import type { CasesState as CasesStateType, InViewContextType, Case } from "../types";
import { stolmc_text, Text } from "../../i18n";

interface CasesStateProps {
  children: ReactNode;
}

export default function CasesState({ children }: CasesStateProps) {
  const inViewContext = useContext(InViewContext) as InViewContextType;
  const currentUserInDisplay = inViewContext.state.userId;

  const initialState: CasesStateType = {
    user: "",
    cases: [],
    loadingCases: false,
  };
  const [state, dispatch] = useReducer(AppReducer, initialState);
  const apiUrlCases = `${stolmcData.root_url}/wp-json/${stolmcData.api_url}/cases`;

  const getCases = async (id: string | number, onlyFetch: boolean): Promise<Case[] | void> => {
    try {
      if (!onlyFetch) {
        dispatch({
          type: GET_CASES,
          payload: {
            user: state.user,
            cases: state.cases,
            loadingCases: true,
          },
        });
      }

      const res = await get(`${apiUrlCases}/${id}`, {
        headers: {
          "X-WP-Nonce": stolmcData.nonce,
        },
      });

      if (!onlyFetch) {
        dispatch({
          type: GET_CASES,
          payload: {
            user: state.user,
            cases: res.data.data,
            loadingCases: false,
          },
        });
      }

      return res.data.data;
    } catch (error) {
      console.log(error);
    }
  };

  const postCase = async (id: string | number, title: string): Promise<void> => {
    if (title === "") {
      alert(stolmc_text(Text.AlertBlankCaseTitle));
      return;
    }

    const dataToPost = { id_user: id, title: title };

    try {
      await post(`${apiUrlCases}/${id}`, dataToPost, {
        headers: {
          "X-WP-Nonce": stolmcData.nonce,
          "Content-type": "application/json",
        },
      });

      const getAllCases = await getCases(id, true);
      const casesArray = Array.isArray(getAllCases) ? getAllCases : [];
      const newCaseAutoGeneratedId = casesArray[casesArray.length - 1]?.id;
      const newCase = {
        id: newCaseAutoGeneratedId,
        id_user: id,
        title: title,
        status: "open",
      };
      const currentCases = state.cases;
      const newCases = [...currentCases, newCase];

      dispatch({
        type: GET_CASES,
        payload: {
          user: id,
          cases: newCases,
          loadingCases: false,
        },
      });

      toast.success(stolmc_text(Text.ToastCaseAdded));
    } catch (error) {
      alert(stolmc_text(Text.AlertErrorBase) + error);
    }
  };

  const toggleCase = async (id: string | number): Promise<void> => {
    try {
      await post(`${apiUrlCases}-status/${id}`, null, {
        headers: {
          "X-WP-Nonce": stolmcData.nonce,
        },
      });

      const currentCases = state.cases;
      const newCases = currentCases.map((theCase: Case) => {
        if (theCase.id.toString() === id.toString()) {
          return { ...theCase, status: theCase.status === "open" ? "close" : "open" };
        }
        return theCase;
      });

      dispatch({
        type: GET_CASES,
        payload: {
          user: state.user,
          cases: newCases,
          loadingCases: false,
        },
      });

      toast.success(stolmc_text(Text.ToastCaseToggled));
    } catch (error) {
      alert(stolmc_text(Text.AlertErrorBase) + error);
    }
  };

  const deleteCase = async (id: string | number, _title: string): Promise<void> => {
    if (!confirm(stolmc_text(Text.ConfirmDeleteCaseMsg))) return;

    try {
      await del(`${apiUrlCases}/${id}`, {
        headers: {
          "X-WP-Nonce": stolmcData.nonce,
        },
      });

      const currentCases = state.cases;
      const newCases = currentCases.filter(
        (theCase: Case) => theCase.id.toString() !== id.toString()
      );

      dispatch({
        type: GET_CASES,
        payload: {
          user: state.user,
          cases: newCases,
          loadingCases: false,
        },
      });

      toast.success(stolmc_text(Text.ToastCaseDeleted));
    } catch (error) {
      alert(stolmc_text(Text.AlertErrorBase) + error);
    }
  };

  const editCase = async (id: string | number, id_user: string | number, newTitle: string): Promise<void> => {
    if (newTitle === "") {
      alert(stolmc_text(Text.AlertBlankCaseTitle));
      return;
    }

    const idTitleObj = { id_user, title: newTitle };

    try {
      await put(`${apiUrlCases}/${id}`, idTitleObj, {
        headers: {
          "X-WP-Nonce": stolmcData.nonce,
          "Content-type": "application/json",
        },
      });

      const currentCases = state.cases;
      const newCases = currentCases.map((theCase: Case) => {
        if (theCase.id.toString() === id.toString()) {
          return { ...theCase, title: newTitle };
        }
        return theCase;
      });

      dispatch({
        type: GET_CASES,
        payload: {
          user: id_user,
          cases: newCases,
          loadingCases: false,
        },
      });

      toast.success(stolmc_text(Text.ToastCaseEdited));
    } catch (error) {
      alert(stolmc_text(Text.AlertErrorBase) + error);
    }
  };

  return (
    <CasesContext.Provider
      value={{
        state,
        getCases,
        postCase,
        toggleCase,
        deleteCase,
        editCase,
        currentUserInDisplay,
        navigate: inViewContext.navigate,
      }}
    >
      {children}
    </CasesContext.Provider>
  );
}
